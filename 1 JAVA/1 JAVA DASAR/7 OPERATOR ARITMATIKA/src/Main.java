public class Main {
    public static void main(String[] args) {
        // OPERASI ARITMATIKA
        int variabel1 = 10;
        int variabel2 = 20;
        int hasil;

        // PENJUMLAHAN
        hasil = variabel1 + variabel2;
        System.out.println(variabel1 + " + " + variabel2 + " = " + hasil);

        // PENGURANGAN
        hasil = variabel1 - variabel2;
        System.out.println(variabel1 + " - " + variabel2 + " = " + hasil);

        // PEMBAGIAN
        hasil = variabel1 / variabel2;
        System.out.println(variabel1 + " / " + variabel2 + " = " + hasil);

        // PERKALIAN
        hasil = variabel1 * variabel2;
        System.out.println(variabel1 + " x " + variabel2 + " = " + hasil);

        // MODULUS
        hasil = variabel1 % variabel2;
        System.out.println(variabel1 + " % " + variabel2 + " = " + hasil);
    }
}